#include <iostream>
#include <stack>     // stack பயன்படுத்த கண்டிப்பா தேவை

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a Stack (உருவாக்குதல்)
    //----------------------------------------------------
    stack<int> s;

    //----------------------------------------------------
    // STEP 2 : push() (மேலே அடுக்குதல்)
    //----------------------------------------------------
    // தட்டுகளை ஒவ்வொன்றாக மேல் நோக்கி அடுக்குகிறோம்.
    s.push(10); // அடியில் இருக்கும்
    s.push(20);
    s.push(30); // இப்போ இதுதான் டாப் (Top)

    //----------------------------------------------------
    // STEP 3 : size() (அடுக்கின் எண்ணிக்கை)
    //----------------------------------------------------
    cout << "Size of stack = " << s.size() << endl;

    //----------------------------------------------------
    // STEP 4 : top() (மேலே இருக்கும் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    // கவனிக்க: vector/array-ல் 'front()', 'back()' என்று இருக்கும். 
    // ஆனால் stack-ல் எப்போதுமே 'top()' மட்டும்தான். ஏன்னா மேல மட்டும்தான் பார்க்க முடியும்.
    cout << "Top Element = " << s.top() << endl;

    //----------------------------------------------------
    // STEP 5 : pop() (மேலே இருக்கும் மதிப்பை நீக்குதல்)
    //----------------------------------------------------
    // கடைசியாக வைக்கப்பட்ட 30 என்ற எண் நீக்கப்படும்.
    s.pop(); 
    
    // இப்போ 30 நீக்கப்பட்டதால், அதற்கு கீழே இருந்த 20 டாப்-க்கு வந்துவிடும்.
    cout << "New Top Element after pop = " << s.top() << endl;

    //----------------------------------------------------
    // STEP 6 : empty() (காலியாக உள்ளதா என சரிபார்த்தல்)
    //----------------------------------------------------
    if(s.empty())
    {
        cout << "Stack is Empty" << endl;
    }
    else
    {
        cout << "Stack is Not Empty" << endl;
    }

    //----------------------------------------------------
    // STEP 7 : Display all elements (அனைத்தையும் அச்சிடுதல்)
    //----------------------------------------------------
    // கவனிக்க: stack-ல் index `[ ]` கிடையாது. range-based for loop-ம் பயன்படுத்த முடியாது!
    // இதில் இருக்கும் மதிப்புகளைப் பார்க்க வேண்டுமென்றால், 'top' மதிப்பை எடுத்துவிட்டு 'pop' செய்துகொண்டே தான் வர வேண்டும்.
    cout << "Remaining Elements in Stack : ";
    while(!s.empty())
    {
        cout << s.top() << " "; // டாப் எலிமெண்ட்டை காட்டும்
        s.pop();               // காட்டிய பின் அதை நீக்கிவிட்டு அடுத்த எலிமெண்ட்டுக்கு நகரும்
    }
    cout << endl;

    // இப்பொழுது லூப் முடிந்து வெளியே வரும்போது ஸ்டேக் முழுவதும் காலியாகி இருக்கும்.
    cout << "Final size after displaying = " << s.size() << endl;

    return 0;
}