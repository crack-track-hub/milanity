#include <iostream>
#include <set>       // multiset பயன்படுத்தவும் set ஹெடரே போதும்
#include <algorithm> // standard algorithms பயன்படுத்த தேவை

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a Multiset (உருவாக்குதல்)
    //----------------------------------------------------
    multiset<int> ms;

    //----------------------------------------------------
    // STEP 2 : insert() (மதிப்புகளை உள்ளே போடுதல்)
    //----------------------------------------------------
    ms.insert(40);
    ms.insert(10);
    ms.insert(30);
    ms.insert(10); // டூப்ளிகேட் எண் - இதுவும் உள்ளே சேரும்
    ms.insert(10); // இன்னுமொரு டூப்ளிகேட் எண்

    //----------------------------------------------------
    // STEP 3 : Display Elements (மதிப்புகளைக் காட்டுதல்)
    //----------------------------------------------------
    // பிரிண்ட் செய்து பார்த்தால் எல்லா 10-களும் இருக்கும், அதே சமயம் 10, 10, 10, 30, 40 என வரிசையாகவும் இருக்கும்.
    cout << "All Elements (Sorted with Duplicates) : ";
    for(int x : ms)
    {
        cout << x << " ";
    }
    cout << endl;

    //----------------------------------------------------
    // STEP 4 : size() (எண்ணிக்கை)
    //----------------------------------------------------
    // டூப்ளிகேட்டும் கணக்கில் எடுத்துக்கொள்ளப்படுவதால் சைஸ் 5 என்று காட்டும்.
    cout << "Size of multiset = " << ms.size() << endl;

    //----------------------------------------------------
    // STEP 5 : count() (ஒரு எண் எத்தனை முறை உள்ளது என எண்ணுதல்)
    //----------------------------------------------------
    // ordinary set-ல் count() பண்ணா 0 அல்லது 1 தான் வரும். ஆனா multiset-ல் அந்த எண் எத்தனை முறை இருக்கிறதோ அந்த எண்ணிக்கையைக் காட்டும்.
    cout << "Number of times 10 is present = " << ms.count(10) << endl;

    //----------------------------------------------------
    // STEP 6 : find() (ஒரு மதிப்பைத் தேடுதல்)
    //----------------------------------------------------
    // இது தேடப்படும் எண் இருக்கும் பல இடங்களில் "முதல்" இடத்தின் (First Instance) ஐடிரேட்டரை கொடுக்கும்.
    auto it = ms.find(10);
    if(it != ms.end())
    {
        cout << "Found 10 in multiset!" << endl;
    }

    //----------------------------------------------------
    // STEP 7 : erase() - வழக்கம் போல் நீக்குதல் (கவனிக்க வேண்டிய இடம்!)
    //----------------------------------------------------
    // மிக முக்கியம்: நீங்கள் நேரடியாக 'ms.erase(10)' என்று கொடுத்தால், 
    // உள்ளே இருக்கும் அனைத்து '10' எண்களும் மொத்தமாக நீக்கப்பட்டுவிடும்!
    // ஒருவேளை உங்களுக்கு ஒரே ஒரு '10'-ஐ மட்டும் தான் நீக்க வேண்டும் என்றால்,
    //  find() செய்த ஐடிரேட்டரை உள்ளே அனுப்ப வேண்டும்.
    
    ms.erase(it); // இது ஒரே ஒரு 10-ஐ மட்டும் நீக்கும் (ஐடிரேட்டர் கொடுத்ததால்)

    cout << "After Erasing single 10 : ";
    for(int x : ms) cout << x << " ";
    cout << endl;

    // நேரடியாக மதிப்பை கொடுத்தால் மீதி இருக்கும் எல்லா 10-களும் காலி ஆகிவிடும்.
    ms.erase(10); 
    cout << "After Erasing all remaining 10s : ";
    for(int x : ms) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 8 : clear() (மொத்தமாக காலி செய்தல்)
    //----------------------------------------------------
    ms.clear();
    cout << "Size after clear() = " << ms.size() << endl;

    return 0;
}