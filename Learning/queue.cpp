#include <iostream>
#include <queue>     // queue பயன்படுத்த கண்டிப்பா தேவை

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a Queue (வரிசையை உருவாக்குதல்)
    //----------------------------------------------------
    queue<int> q;

    //----------------------------------------------------
    // STEP 2 : push() (வரிசையின் கடைசியில் சேர்த்தல்)
    //----------------------------------------------------
    // டிக்கெட் லைனில் ஆட்களை வரிசையாக பின்னாடி சேர்க்கிறோம்.
    q.push(10); // வரிசையில் முதலில் நின்றவர் (Front)
    q.push(20);
    q.push(30); // வரிசையின் கடைசியில் நின்றவர் (Back)

    //----------------------------------------------------
    // STEP 3 : size() (வரிசையின் நீளம்/எண்ணிக்கை)
    //----------------------------------------------------
    cout << "Size of queue = " << q.size() << endl;

    //----------------------------------------------------
    // STEP 4 : front() (வரிசையில் முதலில் நிற்பவரைப் பார்த்தல்)
    //----------------------------------------------------
    // டிக்கெட் கவுண்ட்டருக்கு பக்கத்துல யாரு இருக்கான்னு பார்க்கிறோம்.
    cout << "Front Element = " << q.front() << endl;

    //----------------------------------------------------
    // STEP 5 : back() (வரிசையில் கடைசியில் நிற்பவரைப் பார்த்தல்)
    //----------------------------------------------------
    // லைனோட கடைசியில இப்போ யாரு வந்து நின்னான்னு பார்க்கிறோம்.
    cout << "Back Element = " << q.back() << endl;

    //----------------------------------------------------
    // STEP 6 : pop() (முதலில் நிற்பவரை வரிசையிலிருந்து நீக்குதல்)
    //----------------------------------------------------
    // முதலில் நின்ற 10 என்ற எண்ணுக்கு டிக்கெட் கிடைத்துவிட்டதால் அவர் லைனை விட்டு வெளியேறுகிறார்.
    q.pop(); 
    
    // இப்போ 10 போனதுனால, அவருக்கு பின்னாடி நின்ற 20 பிரண்ட்-க்கு வந்துடுவார்.
    cout << "New Front Element after pop = " << q.front() << endl;

    //----------------------------------------------------
    // STEP 7 : empty() (காலியாக உள்ளதா என சரிபார்த்தல்)
    //----------------------------------------------------
    if(q.empty())
    {
        cout << "Queue is Empty" << endl;
    }
    else
    {
        cout << "Queue is Not Empty" << endl;
    }

    //----------------------------------------------------
    // STEP 8 : Display all elements (அனைத்தையும் அச்சிடுதல்)
    //----------------------------------------------------
    // கவனிக்க: stack மாதிரியே queue-விலும் index `[ ]` கிடையாது. range-based for loop-ம் கிடையாது!
    // இதில் இருக்கும் மதிப்புகளைப் பார்க்க வேண்டுமென்றால், 'front' மதிப்பை எடுத்துவிட்டு 'pop' செய்துகொண்டே தான் வர வேண்டும்.
    cout << "Remaining Elements in Queue : ";
    while(!q.empty())
    {
        cout << q.front() << " "; // முன்னாடி இருக்குறவரை காட்டும்
        q.pop();                 // காட்டிய பின் அவரை நீக்கிவிட்டு அடுத்த ஆளுக்கு நகரும்
    }
    cout << endl;

    // இப்போ கியூ முழுசா காலியாகி இருக்கும்.
    cout << "Final size after displaying = " << q.size() << endl;

    return 0;
}