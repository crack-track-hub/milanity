#include <iostream>
#include <map>       // map பயன்படுத்த கண்டிப்பா தேவை
#include <string>

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a Map (உருவாக்குதல்)
    //----------------------------------------------------
    // map<கீ_வகையீடு, வேல்யூ_வகையீடு> பெயர்;
    // இங்கு மாணவர் ரோல் நம்பர் (int) என்பது Key, அவர் பெயர் (string) என்பது Value.
    map<int, string> student_roll;

    //----------------------------------------------------
    // STEP 2 : insert() & [] (மதிப்புகளை உள்ளே போடுதல்)
    //----------------------------------------------------
    // வழி 1: insert மற்றும் make_pair பயன்படுத்துவது
    student_roll.insert(make_pair(102, "Ramu"));
    student_roll.insert(make_pair(101, "Somu"));

    // வழி 2: அரே இன்டெக்ஸ் `[ ]` போல மிக எளிமையாகப் போடுவது (இதுதான் எல்லாரும் பயன்படுத்துவாங்க)
    student_roll[104] = "Gopal";
    student_roll[103] = "Siva";
    
    // கவனிக்க: ஏற்கனவே உள்ள Key-க்கு புது வேல்யூ கொடுத்தால் அது அப்டேட் ஆகிவிடும்.
    student_roll[102] = "Ramu Updated"; 

    //----------------------------------------------------
    // STEP 3 : Display Elements (மதிப்புகளைக் காட்டுதல்)
    //----------------------------------------------------
    // பிரிண்ட் செய்து பார்த்தால் நாம் மாற்றி மாற்றி போட்டாலும் 101, 102, 103, 104 என Key படி வரிசையாக இருக்கும்.
    // இங்கு 'x.first' என்பது Key-ஐயும், 'x.second' என்பது Value-வையும் குறிக்கும்.
    cout << "All Students (Sorted by Roll No) :\n";
    for(auto x : student_roll)
    {
        cout << "Roll No: " << x.first << " | Name: " << x.second << endl;
    }
    cout << endl;

    //----------------------------------------------------
    // STEP 4 : [] & at() (குறிப்பிட்ட Key-ன் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Roll No 101 Name [] = " << student_roll[101] << endl;
    cout << "Roll No 103 Name (at) = " << student_roll.at(103) << endl;

    //----------------------------------------------------
    // STEP 5 : size() (ஜோடிகளின் எண்ணிக்கை)
    //----------------------------------------------------
    cout << "Total Students (Size) = " << student_roll.size() << endl;

    //----------------------------------------------------
    // STEP 6 : empty() (காலியாக உள்ளதா என சரிபார்த்தல்)
    //----------------------------------------------------
    if(student_roll.empty())
    {
        cout << "Map is Empty" << endl;
    }
    else
    {
        cout << "Map is Not Empty" << endl;
    }

    //----------------------------------------------------
    // STEP 7 : find() (ஒரு Key-ஐத் தேடுதல்)
    //----------------------------------------------------
    // ரோல் நம்பர் 103 இருக்கிறாரா என தேடுகிறோம்.
    auto it = student_roll.find(103);
    if(it != student_roll.end())
    {
        cout << "Found! Student 103 name is: " << it->second << endl;
    }

    //----------------------------------------------------
    // STEP 8 : count() (ஒரு Key உள்ளதா என உறுதி செய்தல்)
    //----------------------------------------------------
    // மேப்பில் டூப்ளிகேட் Key இருக்காது என்பதால், இருந்தால் '1' காட்டும், இல்லை என்றால் '0' காட்டும்.
    if(student_roll.count(104) == 1)
    {
        cout << "Roll No 104 is present in the map" << endl;
    }

    //----------------------------------------------------
    // STEP 9 : erase() (குறிப்பிட்ட Key-ஐ நீக்குதல்)
    //----------------------------------------------------
    // நாம் எதை நீக்க வேண்டுமோ அதனுடைய Key-ஐ மட்டும் கொடுத்தால் போதும், அந்த ஜோடியே நீக்கப்படும்.
    student_roll.erase(102); 

    cout << "\nAfter Erase (Roll No 102) :\n";
    for(auto x : student_roll) cout << x.first << " -> " << x.second << endl;

    //----------------------------------------------------
    // STEP 10 : clear() (மொத்தமாக காலி செய்தல்)
    //----------------------------------------------------
    student_roll.clear();
    cout << "\nSize after clear() = " << student_roll.size() << endl;

    return 0;
}
