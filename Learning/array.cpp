#include <iostream>
#include <array>     // array பயன்படுத்த கண்டிப்பா தேவை
#include <algorithm> // swap பயன்படுத்த தேவை

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create an Array (உருவாக்குதல்)
    //----------------------------------------------------
    // array<தரவு வகை, அளவு> பெயர்; 
    // வெக்டர் மாதிரி இதோட அளவை நடுவுல மாத்த முடியாது. பிக்ஸட் சைஸ் (Fixed Size).
    array<int, 5> numbers = {10, 20, 30, 40, 50};

    //----------------------------------------------------
    // STEP 2 : Display Elements (மதிப்புகளைக் காட்டுதல்)
    //----------------------------------------------------
    cout << "All Elements : ";
    for(int x : numbers)
    {
        cout << x << " ";
    }
    cout << endl;

    //----------------------------------------------------
    // STEP 3 : front() (முதல் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Front = " << numbers.front() << endl;

    //----------------------------------------------------
    // STEP 4 : back() (கடைசி மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Back = " << numbers.back() << endl;

    //----------------------------------------------------
    // STEP 5 : [] (இன்டெக்ஸ் மூலம் மதிப்பை எடுத்தல்/மாற்றுதல்)
    //----------------------------------------------------
    // Index 2-ல் இருக்கும் மதிப்பை 300 ஆக மாற்றுகிறோம்.
    numbers[2] = 300;

    cout << "After Update [] : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 6 : at() (பாதுகாப்பான முறையில் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    // Index 3-ல் இருக்கும் மதிப்பை பிரிண்ட் செய்கிறோம். 
    // இங்கேயும் Index தப்பா இருந்தா இது எர்ரர் காட்டும் (Out of bound check).
    cout << "Element at index 3 (at) = " << numbers.at(3) << endl;

    //----------------------------------------------------
    // STEP 7 : size() (அளவை அறிதல்)
    //----------------------------------------------------
    // அரே-வில் மொத்தம் எத்தனை இடங்கள் (Size) உள்ளது என காட்டும்.
    cout << "Size = " << numbers.size() << endl;

    //----------------------------------------------------
    // STEP 8 : empty() (காலியாக உள்ளதா என சரிபார்த்தல்)
    //----------------------------------------------------
    // அரே எப்போதுமே காலியாக இருக்காது (ஏன்னா சைஸ் பிக்ஸட்), இருந்தாலும் செக் பண்ணலாம்.
    if(numbers.empty())
    {
        cout << "Array is Empty" << endl;
    }
    else
    {
        cout << "Array is Not Empty" << endl;
    }

    //----------------------------------------------------
    // STEP 9 : swap() (இரு Array-களின் மதிப்புகளை மாற்றுதல்)
    //----------------------------------------------------
    // கவனிங்க: இரண்டு அரே-வும் ஒரே சைஸ் (இங்க 5) மற்றும் ஒரே டேட்டா டைப்ல இருக்கணும்.
    array<int, 5> another_array = {1, 2, 3, 4, 5};
    
    numbers.swap(another_array);

    cout << "After Swap (numbers array) : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 10 : fill() (ஒரே மதிப்பை அரே முழுக்க நிரப்புதல்)
    //----------------------------------------------------
    // அரே-வில் இருக்கும் எல்லா இடங்களிலும் '7' என்ற எண்ணை மொத்தமாக நிரப்புகிறோம்.
    numbers.fill(7);

    cout << "After fill(7) : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    return 0;
}