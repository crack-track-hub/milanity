#include <iostream>
#include <queue>     // priority_queue பயன்படுத்தவும் queue ஹெடரே போதும்

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a Priority Queue (உருவாக்குதல்)
    //----------------------------------------------------
    // இது பை-டீஃபால்ட்டாக பெரிய எண்ணை மேலே வைக்கும் (Max Heap)
    priority_queue<int> pq;

    //----------------------------------------------------
    // STEP 2 : push() (மதிப்புகளை உள்ளே போடுதல்)
    //----------------------------------------------------
    // நாம் 30, 10, 50 என்று எந்த ஒரு வரிசையும் இல்லாமல் எண்களை உள்ளே போடுகிறோம்.
    pq.push(30);
    pq.push(10);
    pq.push(50); 

    //----------------------------------------------------
    // STEP 3 : size() (எண்ணிக்கை)
    //----------------------------------------------------
    cout << "Size of priority queue = " << pq.size() << endl;

    //----------------------------------------------------
    // STEP 4 : top() (அதிக முக்கியத்துவம் உள்ள மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    // கவனிக்க: சாதாரண queue-வில் 'front()' இருக்கும். 
    // ஆனால் priority_queue-வில் stack மாதிரி 'top()' தான் பயன்படுத்த வேண்டும்.
    // நாம் 50-ஐ கடைசியாகப் போட்டாலும், அதுதான் பெரிய எண் என்பதால் அதுதான் 'top'-ல் இருக்கும்.
    cout << "Top Element (Highest Priority) = " << pq.top() << endl;

    //----------------------------------------------------
    // STEP 5 : pop() (மேலே இருக்கும் மதிப்பை நீக்குதல்)
    //----------------------------------------------------
    // டாப்-பில் இருக்கும் 50 என்ற எண் நீக்கப்படும்.
    pq.pop(); 
    
    // 50 போனதுக்கு அப்புறம், மீதி இருக்குறதுல பெரிய எண்ணான 30 டாப்-க்கு வந்துடும்.
    cout << "New Top Element after pop = " << pq.top() << endl;

    //----------------------------------------------------
    // STEP 6 : empty() (காலியாக உள்ளதா என சரிபார்த்தல்)
    //----------------------------------------------------
    if(pq.empty())
    {
        cout << "Priority Queue is Empty" << endl;
    }
    else
    {
        cout << "Priority Queue is Not Empty" << endl;
    }

    //----------------------------------------------------
    // STEP 7 : Display all elements (அனைத்தையும் அச்சிடுதல்)
    //----------------------------------------------------
    // இதிலும் index `[ ]` கிடையாது. range-based for loop-ம் கிடையாது!
    // இதில் இருக்கும் மதிப்புகளைப் பார்த்தால் அவை தானாகவே பெரிய எண்ணிலிருந்து சிறிய எண்ணாக (Descending Order) பிரிண்ட் ஆகும்.
    cout << "Remaining Elements (Sorted by Priority) : ";
    while(!pq.empty())
    {
        cout << pq.top() << " "; // டாப் எலிமெண்ட்டை காட்டும்
        pq.pop();               // காட்டிய பின் அதை நீக்கும்
    }
    cout << endl;

    return 0;
}