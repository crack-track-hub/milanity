#include <iostream>
#include <set>       // set பயன்படுத்த கண்டிப்பா தேவை
#include <algorithm> // find இதில் செட்டின் சொந்த find()-ஐ பயன்படுத்துவோம்

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a Set (உруவாக்குதல்)
    //----------------------------------------------------
    set<int> s;

    //----------------------------------------------------
    // STEP 2 : insert() (மதிப்புகளை உள்ளே போடுதல்)
    //----------------------------------------------------
    // கவனிக்க: vector/list மாதிரி இதுல push_back கிடையாது, 'insert' தான் பயன்படுத்த வேண்டும்.
    s.insert(40);
    s.insert(10);
    s.insert(30);
    s.insert(10); // மீண்டும் 10-ஐ போடுகிறோம் (டூப்ளிகேட்)

    //----------------------------------------------------
    // STEP 3 : Display Elements (மதிப்புகளைக் காட்டுதல்)
    //----------------------------------------------------
    // பிரிண்ட் செய்து பார்த்தால் 10 ஒரு முறைதான் இருக்கும், மேலும் எண்கள் 10, 30, 40 என வரிசையாக இருக்கும்.
    cout << "All Elements (Sorted & Unique) : ";
    for(int x : s)
    {
        cout << x << " ";
    }
    cout << endl;

    //----------------------------------------------------
    // STEP 4 : size() (எண்ணிக்கை)
    //----------------------------------------------------
    // நாம் 4 எண்கள் போட்டாலும், டூப்ளிகேட் நீக்கப்பட்டதால் சைஸ் 3 என்று காட்டும்.
    cout << "Size of set = " << s.size() << endl;

    //----------------------------------------------------
    // STEP 5 : empty() (காலியாக உள்ளதா என சரிபார்த்தல்)
    //----------------------------------------------------
    if(s.empty())
    {
        cout << "Set is Empty" << endl;
    }
    else
    {
        cout << "Set is Not Empty" << endl;
    }

    //----------------------------------------------------
    // STEP 6 : find() (ஒரு மதிப்பைத் தேடுதல்)
    //----------------------------------------------------
    // செட்டில் '30' என்ற எண் இருக்கிறதா என தேடுகிறோம்.
    // இது செட்டின் சொந்த .find() என்பதால் மிக மிக வேகமாகத் தேடும்.
    auto it = s.find(30);
    
    if(it != s.end())
    {
        cout << "Element 30 found in set!" << endl;
    }
    else
    {
        cout << "Element not found!" << endl;
    }

    //----------------------------------------------------
    // STEP 7 : count() (ஒரு மதிப்பு உள்ளதா என உறுதி செய்தல்)
    //----------------------------------------------------
    // செட்டில் டூப்ளிகேட் இருக்காது என்பதால், ஒரு எண் இருந்தால் '1' என்று காட்டும், இல்லை என்றால் '0' என்று காட்டும்.
    if(s.count(40) == 1)
    {
        cout << "40 is present in the set" << endl;
    }

    //----------------------------------------------------
    // STEP 8 : erase() (குறிப்பிட்ட மதிப்பை நீக்குதல்)
    //----------------------------------------------------
    // நாம் இன்டெக்ஸ் கொடுக்க தேவையில்லை, 
    // நேரடியாக எந்த எண்ணை நீக்க வேண்டுமோ அதை கொடுக்கலாம்.
    s.erase(30); 

    cout << "After Erase (30) : ";
    for(int x : s) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 9 : clear() (மொத்தமாக காலி செய்தல்)
    //----------------------------------------------------
    s.clear();
    cout << "Size after clear() = " << s.size() << endl;

    return 0;
}