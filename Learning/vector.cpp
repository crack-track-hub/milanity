#include <iostream>
#include <vector>    // Vector பயன்படுத்த கண்டிப்பா தேவை
#include <algorithm> // find, sort, reverse பயன்படுத்த தேவை

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a Vector (உருவாக்குதல்)
    //----------------------------------------------------
    vector<int> numbers;

    //----------------------------------------------------
    // STEP 2 : reserve() (மெமரியை முன்கூட்டியே ஒதுக்குதல்)
    //----------------------------------------------------
    // 10 எண்களைச் சேமிக்க தேவையான இடத்தை ஆரம்பத்திலேயே ஒதுக்குகிறோம்.
    numbers.reserve(10);

    //----------------------------------------------------
    // STEP 3 : push_back() (கடைசியில் சேர்த்தல்)
    //----------------------------------------------------
    numbers.push_back(10);
    numbers.push_back(20);

    //----------------------------------------------------
    // STEP 4 : emplace_back() (நேரடியாக மெமரியில் சேர்த்தல்)
    //----------------------------------------------------
    numbers.emplace_back(30);
    numbers.emplace_back(40);

    //----------------------------------------------------
    // STEP 5 : insert() (குறிப்பிட்ட இடத்தில் செருகுதல்)
    //----------------------------------------------------
    // Vector-ன் தொடக்கத்தில் (Index 0-ல்) 5 என்ற எண்ணைச் செருகுகிறோம்.
    numbers.insert(numbers.begin(), 5);

    //----------------------------------------------------
    // STEP 6 : Display using Range-based for loop
    //----------------------------------------------------
    cout << "All Elements : ";
    for(int x : numbers) {
        cout << x << " ";
    }
    cout << endl;

    //----------------------------------------------------
    // STEP 7 : front() (முதல் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Front Element = " << numbers.front() << endl;

    //----------------------------------------------------
    // STEP 8 : back() (கடைசி மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Back Element = " << numbers.back() << endl;

    //----------------------------------------------------
    // STEP 9 : [] (இன்டெக்ஸ் மூலம் மதிப்பை எடுத்தல்/மாற்றுதல்)
    //----------------------------------------------------
    // Index 2-ல் இருக்கும் மதிப்பை 200 ஆக மாற்றுகிறோம்.
    numbers[2] = 200;

    //----------------------------------------------------
    // STEP 10 : at() (பாதுகாப்பான முறையில் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    // Index 3-ல் இருக்கும் மதிப்பை பிரிண்ட் செய்கிறோம்.
    cout << "Element at index 3 = " << numbers.at(3) << endl;

    //----------------------------------------------------
    // STEP 11 : size() (தற்போதுள்ள எண்களின் எண்ணிக்கை)
    //----------------------------------------------------
    cout << "Size = " << numbers.size() << endl;

    //----------------------------------------------------
    // STEP 12 : capacity() (மொத்த மெமரி கொள்ளளவு)
    //----------------------------------------------------
    cout << "Capacity = " << numbers.capacity() << endl;

    //----------------------------------------------------
    // STEP 13 : empty() (காலியாக உள்ளதா என சரிபார்த்தல்)
    //----------------------------------------------------
    if(numbers.empty()) {
        cout << "Vector is Empty" << endl;
    } else {
        cout << "Vector is Not Empty" << endl;
    }

    //----------------------------------------------------
    // STEP 14 : resize() (அளவை மாற்றுதல்)
    //----------------------------------------------------
    // அளவை 3 ஆகக் குறைக்கிறோம். மீதி உள்ள எண்கள் நீக்கப்படும்.
    numbers.resize(3);
    cout << "After resize(3) : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 15 : pop_back() (கடைசி எண்ணை நீக்குதல்)
    //----------------------------------------------------
    numbers.pop_back(); 
    cout << "After pop_back() : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    // அடுத்த ஸ்டெப்களுக்காக மீண்டும் சில எண்களைச் சேர்க்கிறோம்
    numbers.push_back(50);
    numbers.push_back(10);

    //----------------------------------------------------
    // STEP 16 : erase() (குறிப்பிட்ட இடத்தில் உள்ளதை நீக்குதல்)
    //----------------------------------------------------
    // Index 1-ல் (இரண்டாவது இடம்) இருக்கும் எண்ணை நீக்குகிறோம்.
    numbers.erase(numbers.begin() + 1);

    //----------------------------------------------------
    // STEP 17 : assign() (புதிய மதிப்புகளை மொத்தமாக மாற்றுதல்)
    //----------------------------------------------------
    // பழைய எண்களை நீக்கிவிட்டு, புதுசாக 3 முறை '99' என்ற எண்ணை நிரப்புகிறோம்.
    // (குறிப்பு: கீழே உள்ள code வேலை செய்ய இதை கமெண்ட் செய்துள்ளேன்)
    // numbers.assign(3, 99);

    //----------------------------------------------------
    // STEP 18 : find() (ஒரு மதிப்பைத் தேடுதல்)
    //----------------------------------------------------
    // '50' என்ற எண் Vector-ல் இருக்கிறதா என்று தேடுகிறோம்.
    auto it = find(numbers.begin(), numbers.end(), 50);
    if(it != numbers.end()) {
        cout << "Found 50 in the vector!" << endl;
    }

    //----------------------------------------------------
    // STEP 19 : sort() (வரிசைப்படுத்துதல் - ஏறுவரிசை)
    //----------------------------------------------------
    sort(numbers.begin(), numbers.end());
    cout << "After Sort : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 20 : reverse() (தலைகீழாக மாற்றுதல்)
    //----------------------------------------------------
    reverse(numbers.begin(), numbers.end());
    cout << "After Reverse : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 21 : iterator (ஐடிரேட்டர் பயன்படுத்தி பிரிண்ட் செய்தல்)
    //----------------------------------------------------
    cout << "Display using Iterator : ";
    vector<int>::iterator ptr;
    for(ptr = numbers.begin(); ptr != numbers.end(); ++ptr) {
        cout << *ptr << " ";
    }
    cout << endl;

    //----------------------------------------------------
    // STEP 22 : swap() (இரு Vector-களின் மதிப்புகளை மாற்றுதல்)
    //----------------------------------------------------
    vector<int> another_vector = {7, 8, 9};
    numbers.swap(another_vector);
    cout << "After Swap (First Element of numbers) = " << numbers[0] << endl;

    //----------------------------------------------------
    // STEP 23 : clear() (அனைத்தையும் காலி செய்தல்)
    //----------------------------------------------------
    numbers.clear();
    cout << "Size after clear() = " << numbers.size() << endl;

    return 0;
}