#include <iostream>
#include <list>      // list பயன்படுத்த கண்டிப்பா தேவை
#include <algorithm> // standard algorithms பயன்படுத்த தேவை

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a List (உருவாக்குதல்)
    //----------------------------------------------------
    // list என்பது Doubly Linked List ஆகும். இதில் எண்களை நடுவில் வேகமாய் சேர்க்க/நீக்க முடியும்.
    list<int> numbers;

    //----------------------------------------------------
    // STEP 2 : push_back() (கடைசியில் சேர்த்தல்)
    //----------------------------------------------------
    numbers.push_back(20);
    numbers.push_back(30);

    //----------------------------------------------------
    // STEP 3 : push_front() (முதன் முதலில் சேர்த்தல்)
    //----------------------------------------------------
    // வெக்டர், அரே-வில் இது கிடையாது. லிஸ்ட்டின் தொடக்கத்திலேயே ஒரு எண்ணைச் சேர்க்கிறது.
    numbers.push_front(10);
    numbers.push_front(5);

    cout << "Initial List : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 4 : front() (முதல் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Front Element = " << numbers.front() << endl;

    //----------------------------------------------------
    // STEP 5 : back() (கடைசி மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Back Element = " << numbers.back() << endl;

    //----------------------------------------------------
    // STEP 6 : insert() (குறிப்பிட்ட இடத்தில் செருகுதல்)
    //----------------------------------------------------
    // லிஸ்ட்டின் தொடக்கத்தில் இருந்து 1 இடம் தள்ளி (இரண்டாவது இடத்தில்) 15 என்ற எண்ணைச் செருகுகிறோம்.
    auto it = numbers.begin();
    advance(it, 1); 
    numbers.insert(it, 15);

    cout << "After Insert : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 7 : erase() (குறிப்பிட்ட இடத்தில் உள்ளதை நீக்குதல்)
    //----------------------------------------------------
    // நாம் உருவாக்கிய அதே 'it' பொசிஷனில் (இப்போது இருக்கும் மதிப்பை) நீக்குகிறோம்.
    numbers.erase(it);

    //----------------------------------------------------
    // STEP 8 : remove() (குறிப்பிட்ட மதிப்பைத் தேடி நீக்குதல்)
    //----------------------------------------------------
    // லிஸ்ட்டில் எங்கு '15' இருந்தாலும் அதைத் தேடி மொத்தமாக நீக்கிவிடும்.
    numbers.remove(15);

    //----------------------------------------------------
    // STEP 9 : pop_front() (முதல் எண்ணை நீக்குதல்)
    //----------------------------------------------------
    numbers.pop_front();

    //----------------------------------------------------
    // STEP 10 : pop_back() (கடைசி எண்ணை நீக்குதல்)
    //----------------------------------------------------
    numbers.pop_back();

    cout << "After Pop Front & Back : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    // அடுத்த ஸ்டெப்களுக்காக மீண்டும் சில எண்களைச் சேர்க்கிறோம்
    numbers.push_back(40);
    numbers.push_back(40);
    numbers.push_back(10);

    //----------------------------------------------------
    // STEP 11 : unique() (அடுத்தடுத்து வரும் டூப்ளிகேட்களை நீக்குதல்)
    //----------------------------------------------------
    // பக்கத்து பக்கத்தில் ஒரே எண் (இங்கு 40, 40) திரும்ப வந்தால் ஒன்றை நீக்கிவிடும்.
    numbers.unique();

    cout << "After Unique : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 12 : sort() (வரிசைப்படுத்துதல்)
    //----------------------------------------------------
    // கவனிங்க: லிஸ்ட்டிற்கு `numbers.sort()` என்று தான் பயன்படுத்த வேண்டும்.
    numbers.sort();

    cout << "After Sort : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 13 : reverse() (தலைகீழாக மாற்றுதல்)
    //----------------------------------------------------
    numbers.reverse();

    cout << "After Reverse : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 14 : merge() (இரு லிஸ்ட்டுகளை இணைத்தல்)
    //----------------------------------------------------
    // இரண்டு லிஸ்ட்டுகளும் ஏற்கனவே sort ஆகி இருக்க வேண்டும். 
    list<int> other_list = {5, 25};
    other_list.sort();
    numbers.sort();
    
    // other_list-ல் உள்ளவை numbers-டன் இணைந்து, other_list காலியாகிவிடும்.
    numbers.merge(other_list);

    cout << "After Merge : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 15 : splice() (ஒரு லிஸ்ட்டின் பகுதியை இன்னொன்றுக்கு மாற்றுதல்)
    //----------------------------------------------------
    list<int> wishlist = {100, 200};
    // wishlist-ன் தொடக்க மதிப்பை, numbers-ன் தொடக்கத்தில் வெட்டி ஒட்டுகிறது (Cut & Paste).
    numbers.splice(numbers.begin(), wishlist, wishlist.begin());

    cout << "After Splice : ";
    for(int x : numbers) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 16 : size() (அளவை அறிதல்)
    //----------------------------------------------------
    cout << "Size = " << numbers.size() << endl;

    //----------------------------------------------------
    // STEP 17 : clear() (மொத்தமாக காலி செய்தல்)
    //----------------------------------------------------
    numbers.clear();
    cout << "Size after clear() = " << numbers.size() << endl;

    return 0;
}