#include <iostream>
#include <deque>     // deque பயன்படுத்த கண்டிப்பா தேவை
#include <algorithm> // sort, reverse பயன்படுத்த தேவை

using namespace std;

int main()
{
    //----------------------------------------------------
    // STEP 1 : Create a Deque (உருவாக்குதல்)
    //----------------------------------------------------
    deque<int> dq;

    //----------------------------------------------------
    // STEP 2 : push_back() (கடைசியில் சேர்த்தல்)
    //----------------------------------------------------
    dq.push_back(30);
    dq.push_back(40);

    //----------------------------------------------------
    // STEP 3 : push_front() (முதன் முதலில் சேர்த்தல்)
    //----------------------------------------------------
    // வெக்டரை விட இதில் இதுதான் ஸ்பெஷல்! முன்னாடியே எலிமெண்ட்டுகளைச் சேர்க்கலாம்.
    dq.push_front(20);
    dq.push_front(10);

    //----------------------------------------------------
    // STEP 4 : Display Elements (மதிப்புகளைக் காட்டுதல்)
    //----------------------------------------------------
    cout << "All Elements : ";
    for(int x : dq)
    {
        cout << x << " ";
    }
    cout << endl;

    //----------------------------------------------------
    // STEP 5 : front() (முதல் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Front = " << dq.front() << endl;

    //----------------------------------------------------
    // STEP 6 : back() (கடைசி மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Back = " << dq.back() << endl;

    //----------------------------------------------------
    // STEP 7 : [] (இன்டெக்ஸ் மூலம் மதிப்பை எடுத்தல்/மாற்றுதல்)
    //----------------------------------------------------
    // Index 2-ல் இருக்கும் மதிப்பை 300 ஆக மாற்றுகிறோம்.
    dq[2] = 300;

    cout << "After Update [] : ";
    for(int x : dq) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 8 : at() (பாதுகாப்பான முறையில் மதிப்பை எடுத்தல்)
    //----------------------------------------------------
    cout << "Element at index 1 (at) = " << dq.at(1) << endl;

    //----------------------------------------------------
    // STEP 9 : size() (அளவை அறிதல்)
    //----------------------------------------------------
    cout << "Size = " << dq.size() << endl;

    //----------------------------------------------------
    // STEP 10 : empty() (காலியாக உள்ளதா என சரிபார்த்தல்)
    //----------------------------------------------------
    if(dq.empty())
    {
        cout << "Deque is Empty" << endl;
    }
    else
    {
        cout << "Deque is Not Empty" << endl;
    }

    //----------------------------------------------------
    // STEP 11 : pop_front() (முதல் எலிமெண்ட்டை நீக்குதல்)
    //----------------------------------------------------
    dq.pop_front();

    //----------------------------------------------------
    // STEP 12 : pop_back() (கடைசி எலிமெண்ட்டை நீக்குதல்)
    //----------------------------------------------------
    dq.pop_back();

    cout << "After Pop Front & Back : ";
    for(int x : dq) cout << x << " ";
    cout << endl;

    // அடுத்த ஸ்டெப்களுக்காக மீண்டும் சில எண்களைச் சேர்க்கிறோம்
    dq.push_back(50);
    dq.push_front(5);

    //----------------------------------------------------
    // STEP 13 : insert() (குறிப்பிட்ட இடத்தில் செருகுதல்)
    //----------------------------------------------------
    // தொடக்கத்தில் இருந்து 1 இடம் தள்ளி (Index 1-ல்) 99 என்ற எண்ணைச் செருகுகிறோம்.
    dq.insert(dq.begin() + 1, 99);

    //----------------------------------------------------
    // STEP 14 : erase() (குறிப்பிட்ட இடத்தில் உள்ளதை நீக்குதல்)
    //----------------------------------------------------
    // Index 2-ல் உள்ள எலிமெண்ட்டை நீக்குகிறோம்.
    dq.erase(dq.begin() + 2);

    cout << "After Insert & Erase : ";
    for(int x : dq) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 15 : sort() (வரிசைப்படுத்துதல்)
    //----------------------------------------------------
    sort(dq.begin(), dq.end());
    cout << "After Sort : ";
    for(int x : dq) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 16 : reverse() (தலைகீழாக மாற்றுதல்)
    //----------------------------------------------------
    reverse(dq.begin(), dq.end());
    cout << "After Reverse : ";
    for(int x : dq) cout << x << " ";
    cout << endl;

    //----------------------------------------------------
    // STEP 17 : clear() (மொத்தமாக காலி செய்தல்)
    //----------------------------------------------------
    dq.clear();
    cout << "Size after clear() = " << dq.size() << endl;

    return 0;
}